package DoWhile;

public class DoWhile {
    public static void main (String[] args){
        int i = 2;
        /**
         * A diferencia del While aca primero se ejecuta el codigo y luego se evalua la condicion
         */
        do{
            System.out.println(i);
            i++;
        }while (i < 5);//Dentro de los parentesis ponemos la condicion que queremos evaluar
        //Sintaxis del Do While
    }
}
